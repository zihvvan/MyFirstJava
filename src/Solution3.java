public class Solution3 {
    public static void main(String[] args) {
        // Boolean (참/거짓)
        // 파이썬과 다른 점 : True, False
        // 자바는? : true, false
        System.out.println(true);
        System.out.println(false);
        System.out.println(3 > 5);
        System.out.println(3 < 5);
        System.out.println(3 == 5);
        System.out.println(3 != 5);
        System.out.println(3 >= 5);
        System.out.println(3 <= 5);
        // 파이썬에서는? and, or, not
        System.out.println(3 == 3 & 4 == 4); // and 연산, 그리고 연산.
        System.out.println(3 == 3 && 4 == 4); // 빠른 and 연산
        System.out.println(3 == 3 | 4 == 4); // or 연산, 또는 연산.
        System.out.println(3 == 3 || 4 == 4); // 빠른 or 연산

    }
}
